// BootstrapOverkillHome creates the essential files in ~/.overkill/ that
// Overkill reads on startup. Safe to call repeatedly — existing user-edited
// files are never overwritten.
func BootstrapOverkillHome(homeDir string) error {
	if err := os.MkdirAll(homeDir, 0o750); err != nil {
		return fmt.Errorf("bootstrap: creating home dir: %w", err)
	}

	// Required subdirectories
	dirs := []string{
		"memories",
		"journal/raw",
		"journal/entries",
		"journal/observations",
		"introspection",
		"plans",
		"checkpoints",
		"sessions",
		"skills",
	}
	for _, d := range dirs {
		if err := os.MkdirAll(filepath.Join(homeDir, d), 0o755); err != nil {
			return fmt.Errorf("bootstrap: creating %s: %w", d, err)
		}
	}

	// Unpack bundled skills into ~/.overkill/skills/ on first run.
	// Existing user-installed skills are never overwritten — only
	// skills that don't already exist on disk are extracted.
	if err := unpackBundledSkills(homeDir); err != nil {
		return fmt.Errorf("bootstrap: unpacking skills: %w", err)
	}

	// soul.md — only write if it doesn't exist (user may have edited it)
	soulPath := filepath.Join(homeDir, "memories", "soul.md")
	if _, err := os.Stat(soulPath); os.IsNotExist(err) {
		if err := os.WriteFile(soulPath, []byte(defaultSoulMD), 0o600); err != nil {
			return fmt.Errorf("bootstrap: writing soul.md: %w", err)
		}
	}

	// CLAUDE.md — only write if it doesn't exist (user may have customised it)
	claudePath := filepath.Join(homeDir, "CLAUDE.md")
	if _, err := os.Stat(claudePath); os.IsNotExist(err) {
		if err := os.WriteFile(claudePath, []byte(defaultClaudeMD()), 0o600); err != nil {
			return fmt.Errorf("bootstrap: writing CLAUDE.md: %w", err)
		}
	}

	// Introspection files — generate if missing
	introDir := filepath.Join(homeDir, "introspection")
	introFiles := map[string]string{
		"MODEL_CARD.md":   "# MODEL_CARD.md\n\n> Current model capabilities, limitations, pricing. Auto-generated.\n\nRun `overkill introspect` to populate this file with live data.\n",
		"KNOWN_ISSUES.md": "# KNOWN_ISSUES.md\n\n> Known bugs, gotchas, workarounds. Updated from journal pattern_detected alerts.\n\nNo issues recorded yet.\n",
	}
	for name, content := range introFiles {
		path := filepath.Join(introDir, name)
		if _, err := os.Stat(path); os.IsNotExist(err) {
			if err := os.WriteFile(path, []byte(content), 0o600); err != nil {
				return fmt.Errorf("bootstrap: writing %s: %w", name, err)
			}
		}
	}

	return nil
}

// unpackBundledSkills extracts skills embedded at build time into
// ~/.overkill/skills/. Existing directories are never overwritten —
// users can edit installed skills without losing changes on upgrade.
func unpackBundledSkills(homeDir string) error {
	skillsDir := filepath.Join(homeDir, "skills")
	entries, err := fs.ReadDir(bundled.SkillsFS, "skills")
	if err != nil {
		return fmt.Errorf("reading embedded skills: %w", err)
	}
	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}
		target := filepath.Join(skillsDir, entry.Name())
		// Never overwrite: if the skill already exists (user-installed
		// or previously unpacked), skip it.
		if _, err := os.Stat(target); err == nil {
			continue
		}
		if err := copyEmbeddedDir("skills/"+entry.Name(), target); err != nil {
			return fmt.Errorf("unpacking %s: %w", entry.Name(), err)
		}
	}
	return nil
}

func copyEmbeddedDir(src, dst string) error {
	entries, err := fs.ReadDir(bundled.SkillsFS, src)
	if err != nil {
		return err
	}
	if err := os.MkdirAll(dst, 0o755); err != nil {
		return err
	}
	for _, entry := range entries {
		srcPath := src + "/" + entry.Name()
		dstPath := filepath.Join(dst, entry.Name())
		if entry.IsDir() {
			if err := copyEmbeddedDir(srcPath, dstPath); err != nil {
				return err
			}
			continue
		}
		data, err := fs.ReadFile(bundled.SkillsFS, srcPath)
		if err != nil {
			return err
		}
		if err := os.WriteFile(dstPath, data, 0o644); err != nil {
			return err
		}
	}
	return nil
}
